// Operadores de comparação
// > Maior que
// >= Maior ou igual a
// < Menor que
// <= Menor ou igual a
// == Igualdade (valor)
// === Igualdade estrita (valor e tipo)
// != Diferença (valor)
// !== Diferença estrita (valor e tipo)

console.log(10 > 10);
console.log(10 >= 5);
console.log(10 < 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 === 5);
console.log(10 != 5);
console.log(10 !== 5);
